class SelectForm(forms.Form):
    """index.htmlから選択されたデータを、viewに返せるようにする
    
    Returns:
        list: indexページで選択されたデータ
    """
    goal = forms.ChoiceField(
        choices=GOAL, required=False, widget=forms.Select(attrs={'class': 'cp_ipselect01 cp_sl01'}))
    sex = forms.ChoiceField(
        choices=SEX, required=False, widget=forms.Select(attrs={'class': 'cp_ipselect02 cp_sl02'}))
    old = forms.ChoiceField(
        choices=OLD, required=False, widget=forms.Select(attrs={'class': 'cp_ipselect03 cp_sl03'}))
    height = forms.ChoiceField(
        choices=HEIGHT, required=False, widget=forms.Select(attrs={'class': 'cp_ipselect04 cp_sl04'}))
    weight = forms.ChoiceField(
        choices=WEIGHT, required=False, widget=forms.Select(attrs={'class': 'cp_ipselect05 cp_sl05'}))

    def clean(self):
        # 上記のデータをリスト型に、それぞれ取り出す
        data = super().clean()
        return data
